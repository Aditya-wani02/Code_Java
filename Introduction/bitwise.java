public class bitwise {
    public static void main(String[] args) {
        int a = 3 ,b= 4;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(~a);
        System.out.println(a^b);
        System.out.println(10>>1);
        System.out.println(10<<1);
    }
}
